package week2.day2;

public class Loops {

	public static void main(String[] args) {

		// for
		for (int i = 0; i <=0; i++) {
			System.out.println(i);
		}

		// while -> check conditions at the beginning w/o execution
		int j = 0;
		while(j<=10) {
			System.out.println(j);
			j++;
		}
		
		// do-while -> one execution, only after it checks the conditions
		int k = 0;
		do {
			System.out.println(k);
		}while (k<=10);
		
		//foreach -> use when you don't know the size of the array 

	}

}
